    <h3 class="title">Как происходит восстановление ванны эмалью</h3>
    <p class="text">Вся процедура занимает пару часов, после чего ванна остается сохнуть на сутки-двое. Согласитесь, что это несущественные временные затраты, при этом от вас не потребуется никаких усилий — мастер сам зачистит и подготовит поверхность, закроет плитку от возможных пятен. Состав закроет все пятна плесени, въевшегося налета, желтизны. Также под эмалью исчезнут царапины и сколы, что продлит срок службы всей конструкции.</p>
    <p class="text">Эмалевое покрытие ложится равномерно даже на самую изношенную поверхность, обеспечивая превосходное сцепление и стойкость в течение 2-5 лет, в зависимости от условий эксплуатации. Что дает такая процедура? Помимо визуально опрятной ванны, в которой приятно купаться и купать детей, стирать деликатные вещи, вы также сможете избавиться от риска распространения грибка в санузле. Принимать долгие ванны станет гораздо приятнее, ведь толщина стенок чаши немного возрастет после нанесения покрытия — вода будет дольше оставаться горячей и меньше шуметь при наполнении ванны.</p>
    <h3 class="title">Технология эмалирования ванны</h3>
    <p class="text">Мастер в первую очередь осматривает ванну на наличие дефектов (сколов, трещин, шершавости, водного камня, потёков). Затем, при помощи шлифовальной машины, устраняет все вышеперечисленные недостатки. Далее специалист моет ванну специальными пастами и абразивными средствами для получения белоснежной матовой поверхности. После этой процедуры, ванна обрабатывается щелочью и насухо вытирается хлопчатобумажной ветошью. Заклеив скотчем стены по периметру ванны (во избежание попадания на них эмали), мастер готовит грунтовочный состав эмали. После нанесения его на ванну, мастер приготавливает эмаль для покрытия основным слоем. По технологии, второй слой наносится не ранее, чем через 20 минут</p>
    <p class="text">По окончании всех работ, мастер осматривает ванну на наличие попавших во время эмалировки волосков и крошек. После осмотра, ванна будет готова к эксплуатации через 72 часа(зависит от материала).</p>
    <h3 class="title">Достоинства:</h3>
    <p class="text">- работа занимает всего около 3-5 часов</p>
    <p class="text">- нет необходимости демонтировать сантехнику, трубы, бордюры вокруг ванны</p>
    <p class="text">- цена на услугу ниже, чем у остальных вариантов данного ремонта</p>
    <p class="text">- возможность повторного восстановления</p>
    <p class="text">- идеальный внешний вид готового изделия</p>
    <h3 class="title">Недостатки:</h3>
    <p class="text">- полное высыхание эмали занимает 72 часа</p>
    <p class="text">- не выдерживает постоянного использования агрессивных щелочных и кислотных средств для мытья ванны (например «Туалетный утёнок»)</p>
    <p class="text">- нет устойчивости к сильным ударам и падениям тяжёлых металлических предметов</p>
    <h3 class="title">В каких случаях подходит данный вид восстановления ванны</h3>
    <p class="text">Чаще всего люди выбирают данный метод когда необходимо быстро получить новую ванну с минимальными затратами. Идеально подходит в случае продажи, сдачи квартиры в аренду и тд. </p>
