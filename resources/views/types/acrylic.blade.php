    <h3 class="title">Как происходит восстановление ванны наливным акрилом</h3>
    <p class="text">Покрытие ванны акрилом – популярная технология восстановления эмали, благодаря которой старая чугунная ванна со сколами, пятнами и потеками ржавчины, в считанные часы превращается в новую.</p>
    <p class="text">Метод заключается в нанесении на внутреннюю поверхность металлической или чугунной ванны нового покрытия - наливного акрила. Реставрация проводится методом налива на дому у заказчика без демонтажа самой ванны.</p>
    <h3 class="title">Технология реставрации ванны наливным акрилом</h3>
    <p class="text">
        Процесс реставрации ванны наливным акрилом делится на три основных этапа.
    </p>
    <p class="text">
        1. Подготовка поверхности.
    </p>
    <p class="text">
        2. Подготовка материала.
    </p>
    <p class="text">
        3. Нанесение материала.
    </p>
    <p class="text">
        На первом этапе мастеру необходимо тщательным образом произвести зачистку ванны.
        Необходимо сделать ванну матовой.
        После обработки ванна должна иметь царапины, шероховатости.
        Это залог хорошей адгезии материала к поверхности ванны. Затем необходимо с использованием моющего средства тщательно промыть и высушить ванну.
        Если на поверхности имеются сколы, они устраняются при помощи автомобильной шпатлевки.
    </p>
    <p class="text">
        После подготовки поверхности можно переходить к материалу.
        Наливной акрил - это двухкомпонентный материал, состоящий из основы и отвердителя.
        Перемешивание компонентов производится вручную в течении 10 минут.
        После этого материалу нужно настоятся примерно 10 минут(в зависимости от марки материала).
        За это время мастеру нужно застелить полы и обклеить стены газетами при необходимости.
    </p>
    <p class="text">
        Далее мастер приступает к нанесению материала на ванну. Первым делом акрил наливается на борта,
        после чего под собственным весом начинает стекать ко дну, оставляя за собой идеальный и ровный слой без подтеков и разводов.
    </p>
    <h3 class="title">Достоинства:</h3>
    <p class="text">- быстрое застывание состава. В зависимости от температуры воздуха в помещении полная полимеризация проходит от 8 до 24 часов.</p>
    <p class="text">- одинаковая толщина слоя. При покрытии ванны наливным акрилом не используются кисти и валики – смесь стекает под собственным весом, равномерно распределяясь по поверхности</p>
    <p class="text">- эффектный внешний вид. Поверхность чаши ванной становится белоснежной и гладкой, жидкий акрил скрывает все сколы, трещины и другие дефекты</p>
    <p class="text">- акриловая поверхность обладает приятными тактильными качествами, а ванна получает дополнительную тепло и шумоизоляцию</p>
    <p class="text">- жидким акрилом можно покрывать любые ванны: чугунные, стальные, акриловые</p>
    <h3 class="title">Недостатки:</h3>
    <p class="text">- покрытие требует аккуратного использования</p>
    <p class="text">- впитывает краску для волос(окрашивается)</p>
    <p class="text">- требуется снятие сифона</p>
